public class Test20 {
	public static void main(String[] args) {

		// while 문 
		int a = 0;	// 초기식 
		while(a < 5) { // 조건식 
			System.out.println("안녕하셔요!");
			a++; //  증감식
		} // 0 1 2 3 4 
		
		System.out.println("===========");
		// 0 ~ 5 
		int i = 0; 
		while(i <= 5) {
			System.out.println(i);
			i++;
		}
		
		
		
		
		
		
		
		
		
		
		
	}
}
